package com.company.conditional;

public class WhileLoop {
    public static void main(String[] args) {

        //   USE WHILE INSTEAD OF FOR IN
             // CASE YOU DON'T KNOW HOW MANY TIMES THE LOOP IS GONNA RUN


        /*
           Syntax of while loop

           initialization;
           while(condition){
                // body
                increment/decrement

           }
         */

        int num=1;
        while(num<5){
            System.out.println(num);
            num++;
        }



    }
}
